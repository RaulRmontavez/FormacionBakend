package org.example;

public class Calculadora {
    public int suma(int uno, int dos){
        return uno + dos;
    }

    public int resta(int uno, int dos){
        return uno - dos;
    }

    public double division(double uno, double dos){
        return uno / dos;
    }

    public int multiplicacion(int uno, int dos){
        return uno * dos;
    }
}
